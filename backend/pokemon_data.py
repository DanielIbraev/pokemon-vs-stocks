EARLIEST_DATE = "1999-01-01"

AVAILABLE_POKEMON = {k: v["name"] for k, v in POKEMON_CARDS.items()}


def get_pokemon_prices(pokemon_id, start, end):
    card = POKEMON_CARDS.get(pokemon_id)
    if card is None:
        return None

    dates = [pd.Timestamp(d) for d, _ in card["prices"]]
    prices = [p for _, p in card["prices"]]

    date_range = pd.date_range(
        start=max(pd.Timestamp(start), pd.Timestamp(EARLIEST_DATE)),
        end=min(pd.Timestamp(end), pd.Timestamp(card["prices"][-1][0])),
        freq="MS"
    )

    interpolated = np.interp(
        [d.timestamp() for d in date_range],
        [d.timestamp() for d in dates],
        prices
    )

    return pd.DataFrame({"date": date_range, "price": interpolated})
